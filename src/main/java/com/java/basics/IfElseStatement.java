package com.java.basics;

public class IfElseStatement {

	public static void main(String[] args) {

		int marks = 40;

		if (marks >= 30) {
			System.out.println("Pass");
		} else {
			System.out.println("Fail");
		}

		int number = 10;

		if (number % 2 == 0) {
			System.out.println("Even Number");
		} else {
			System.out.println("Odd Number");
		}
	}

}
